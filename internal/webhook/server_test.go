func TestIsOwnedRecord_owned(t *testing.T) {
	r := technitium.Record{Comments: "Generated by external-dns-technitium-webhook (resource: foo)"}
	if !isOwnedRecord(r) {
		t.Error("expected record to be owned")
	}
}

func TestIsOwnedRecord_external(t *testing.T) {
	r := technitium.Record{Comments: "managed by someone else"}
	if isOwnedRecord(r) {
		t.Error("expected record to be external")
	}
}

func TestIsAddressRecord(t *testing.T) {
	for _, tc := range []struct {
		rtype string
		want  bool
	}{
		{"A", true},
		{"AAAA", true},
		{"a", true},
		{"CNAME", false},
		{"TXT", false},
		{"MX", false},
	} {
		if got := isAddressRecord(tc.rtype); got != tc.want {
			t.Errorf("isAddressRecord(%q) = %v, want %v", tc.rtype, got, tc.want)
		}
	}
}

// ---- helpers ----

func makeARecord(name, ip string, ttl int64) technitium.Record {
	return technitium.Record{
		Name:  name,
		Type:  "A",
		TTL:   ttl,
		RData: json.RawMessage(`{"ipAddress":"` + ip + `"}`),
	}
}

func findEndpoint(eps []*Endpoint, name string) *Endpoint {
	for _, ep := range eps {
		if ep.DNSName == name {
			return ep
		}
	}
	return nil
}
