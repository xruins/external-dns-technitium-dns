// makeTestServer builds a Server wired with the given zone and domain filters.
// The Technitium client is intentionally nil because these tests only cover handlers
// that do not call the Technitium API.
func makeTestServer(zone string, filters []string) *Server {
	cfg := &config.Config{Zone: zone, DomainFilters: filters}
	return NewServer(cfg, nil)
}

func TestHandleNegotiate_defaultsToZone(t *testing.T) {
	srv := makeTestServer("example.com", nil)

	req := httptest.NewRequest(http.MethodGet, "/", nil)
	w := httptest.NewRecorder()
	srv.ServeHTTP(w, req)

	if w.Code != http.StatusOK {
		t.Fatalf("expected 200, got %d", w.Code)
	}

	var df DomainFilter
	if err := json.NewDecoder(w.Body).Decode(&df); err != nil {
		t.Fatalf("decoding response: %v", err)
	}
	if len(df.Filters) != 1 || df.Filters[0] != "example.com" {
		t.Errorf("unexpected filters: %v", df.Filters)
	}
}

func TestHandleNegotiate_explicitFilters(t *testing.T) {
	srv := makeTestServer("example.com", []string{"a.example.com", "b.example.com"})

	req := httptest.NewRequest(http.MethodGet, "/", nil)
	w := httptest.NewRecorder()
	srv.ServeHTTP(w, req)

	var df DomainFilter
	if err := json.NewDecoder(w.Body).Decode(&df); err != nil {
		t.Fatalf("decoding response: %v", err)
	}
	if len(df.Filters) != 2 {
		t.Errorf("expected 2 filters, got %v", df.Filters)
	}
}

func TestHandleAdjustEndpoints_passThrough(t *testing.T) {
	srv := makeTestServer("example.com", nil)

	body := `[{"dnsName":"www.example.com","targets":["1.2.3.4"],"recordType":"A"}]`
	req := httptest.NewRequest(http.MethodPost, "/adjustendpoints", strings.NewReader(body))
	req.Header.Set("Content-Type", "application/json")
	w := httptest.NewRecorder()
	srv.ServeHTTP(w, req)

	if w.Code != http.StatusOK {
		t.Fatalf("expected 200, got %d", w.Code)
	}

	var endpoints []*Endpoint
	if err := json.NewDecoder(w.Body).Decode(&endpoints); err != nil {
		t.Fatalf("decoding response: %v", err)
	}
	if len(endpoints) != 1 || endpoints[0].DNSName != "www.example.com" {
		t.Errorf("unexpected endpoints: %v", endpoints)
	}
}

func TestRecordsToEndpoints_groupsByNameAndType(t *testing.T) {
	records := []technitium.Record{
		makeARecord("www.example.com", "1.2.3.4", 3600),
		makeARecord("www.example.com", "5.6.7.8", 3600),
		makeARecord("api.example.com", "9.10.11.12", 300),
	}

	endpoints := recordsToEndpoints(records)

	if len(endpoints) != 2 {
		t.Fatalf("expected 2 endpoints, got %d", len(endpoints))
	}

	www := findEndpoint(endpoints, "www.example.com")
	if www == nil {
		t.Fatal("www.example.com endpoint not found")
	}
	if len(www.Targets) != 2 {
		t.Errorf("expected 2 targets for www, got %d", len(www.Targets))
	}
}

func TestRecordsToEndpoints_skipsDisabled(t *testing.T) {
	records := []technitium.Record{
		{Name: "disabled.example.com", Type: "A", TTL: 300, Disabled: true,
			RData: json.RawMessage(`{"ipAddress":"1.2.3.4"}`)},
	}
	endpoints := recordsToEndpoints(records)
	if len(endpoints) != 0 {
		t.Errorf("expected 0 endpoints, got %d", len(endpoints))
	}
}

