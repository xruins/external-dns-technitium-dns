func TestBuildComment_usesResourceLabel(t *testing.T) {
	ep := &Endpoint{
		DNSName: "www.example.com",
		Labels:  map[string]string{"external-dns/resource": "service/default/my-svc"},
	}
	comment := buildComment(ep)
	if !strings.Contains(comment, "service/default/my-svc") {
		t.Errorf("expected resource label in comment, got: %s", comment)
	}
}

